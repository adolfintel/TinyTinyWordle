void ssd1306_sendData(const uint8_t *c, uint16_t n, uint8_t op) {
  uint8_t bytesOut = 0x20;
  while (n--) {
    if (bytesOut & 0x20) {
      Wire.endTransmission();
      Wire.beginTransmission(DISPLAY_ADDRESS);
      Wire.write(op);
      bytesOut = 1;
    }
    Wire.write(*(c++));
    bytesOut++;
  }
  Wire.endTransmission();
}

